    public void llenarComboBoxE (){
        for (Equipo e: listaEquipos){
            jc_equipos.addItem(e.nombre);
        }
    }
    
    public void llenarComboBoxM (){
        for (Equipo e: listaEquipos){
            jc_equiposM.addItem(e.nombre);
        }
    }
    public Equipo getEquipo(String name){
        for (Equipo e: listaEquipos){
            if (e.nombre.equalsIgnoreCase(name)){
                return e;
            }
        }
        return null;
    }
    
    public boolean existsInFile (String name){
        BufferedReader buff;
        try {
            buff = new BufferedReader(new FileReader(equipoFile));
            String letras;
            while((letras=buff.readLine())!=null){
                if (letras.contains(name)){
                    return true;
                }

        }
        } catch (IOException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
        
    }
    
    

